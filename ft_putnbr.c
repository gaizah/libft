#include "libft.h"

int		ft_putnbr(int n)
{
	int temp;

	if (n < 10)
	{
		ft_putchar('0' + n);
		return ;
	}
	temp = (n % 10);
	ft_putnbr(n / 10);
	ft_putchar('0' + temp);
}
