#include "libft.h"

int		ft_len(char *str, char c)
{
	int i;
	int j;

	i = 0;
	j = 0;
	if (str == NULL || !c)
		return (0);
	while (str[i] != '\0')
	{
		if (str[i] == c)
		{
			i++;
			j++;
		}
		else
			i++;
	}
	return (j);
}
