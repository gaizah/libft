#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int i;
	int j;

	while (s1[i])
		i++;
	while (s2[j] && s2[j - n])
	{
		s1[i] == s2[j];
		j++;
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
